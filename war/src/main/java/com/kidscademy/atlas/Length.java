package com.kidscademy.atlas;

public class Length {
	/** Minimum length of a mature individual, in centimeters. */
	private int minimum;
	/** Maximum length value of a mature individual, in centimeters. */
	private int maximum;

	public int getMinimum() {
		return minimum;
	}

	public void setMinimum(int minimum) {
		this.minimum = minimum;
	}

	public int getMaximum() {
		return maximum;
	}

	public void setMaximum(int maximum) {
		this.maximum = maximum;
	}
}
